package com.jason.stock.bean;

public class UpdateChipInLog {

	private String id;
	private int highProfit;
	private int highProfitDay;
	private int lowProfit;
	private int lowProfitDay;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getHighProfit() {
		return highProfit;
	}
	public void setHighProfit(int highProfit) {
		this.highProfit = highProfit;
	}
	public int getHighProfitDay() {
		return highProfitDay;
	}
	public void setHighProfitDay(int highProfitDay) {
		this.highProfitDay = highProfitDay;
	}
	public int getLowProfit() {
		return lowProfit;
	}
	public void setLowProfit(int lowProfit) {
		this.lowProfit = lowProfit;
	}
	public int getLowProfitDay() {
		return lowProfitDay;
	}
	public void setLowProfitDay(int lowProfitDay) {
		this.lowProfitDay = lowProfitDay;
	}
	
}
